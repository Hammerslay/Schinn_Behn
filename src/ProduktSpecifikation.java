
public class ProduktSpecifikation {
	
	private String ingredienser;
	private String tillverkningsmetod;
	
	
	public String getIngredienser() {
		return ingredienser;
	}
	public void setIngredienser(String ingredienser) {
		this.ingredienser = ingredienser;
	}
	public String getTillverkningsmetod() {
		return tillverkningsmetod;
	}
	public void setTillverkningsmetod(String tillverkningsmetod) {
		this.tillverkningsmetod = tillverkningsmetod;
	}

}
